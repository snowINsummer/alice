package alice.entity;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import java.util.Date;

@Entity
public class DailyMailSenderRecord {

    @Id
    @GeneratedValue
    private Integer id;

    @Column(nullable = false)
    private Integer mailSenderInfoId;

    @Column(nullable = false)
    private String dateString;

    @Column(nullable = false)
    private Integer status;

    private Date createTime;

    private Date updateTime;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getMailSenderInfoId() {
        return mailSenderInfoId;
    }

    public void setMailSenderInfoId(Integer mailSenderInfoId) {
        this.mailSenderInfoId = mailSenderInfoId;
    }

    public String getDateString() {
        return dateString;
    }

    public void setDateString(String dateString) {
        this.dateString = dateString;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public Date getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(Date updateTime) {
        this.updateTime = updateTime;
    }
}
